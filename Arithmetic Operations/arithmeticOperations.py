
# Addition
print('addition:', 10 + 3)
# Subtraction
print('Subtraction:', 10 - 3)
# Multiplication
print('Multiplication:', 10 * 3)
# Division with remainder in float format
print('Division in float format:', 10 / 3)
# Division with whole numbers
print('Division with whole number:', 10 // 3)
# Modulus
print('Reminder:', 10 % 3)
# Power set form
print('Power form or exponential:', 10 ** 3)

# Increment form
x = 10
x = x + 3
print(x)
# increment simplified
x = 20
x += 3
print(x)
# Same as the decrement can be:-
x = 12
x -= 4
print(x)

